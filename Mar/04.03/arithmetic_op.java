class arithmetic_op{
	public static void main(String args[]){
	int x;//declaration
	int y;//initialization
	x=10;
	y=5;
	
	System.out.println("x = " + x);
	System.out.println("x = " + y);
	
	System.out.println("x + y: " + (x+y));
	System.out.println("x - y: " + (x-y));
	System.out.println("x * y: " + (x*y));
	System.out.println("x / y: " + (x/y));
	System.out.println("x % y: " + (x%y));
	}
}

