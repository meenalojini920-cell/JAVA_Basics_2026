class data_type{
	public static void main(String args[]){
		byte b1;
		b1=12;
		System.out.println("byte:"+b1);
		
		short s1;
		s1=32000;
		System.out.println("short:"+s1);
		
		int i;
		i=650022;
		System.out.println("int:"+i);
		
		long L;
		L=650226558656L;
		System.out.println("long:"+L);
		
		float f1;
		f1=25.6f;
		System.out.println("float:"+f1);
		
		double db;
		db=86.35D;
		System.out.println("double:"+db);
		
		boolean bool1;
		bool1=true;
		System.out.println("boolean:"+bool1);
		
		char ch;
		ch='A';
		System.out.println("char:"+ch);
		
		char ch1;
		ch1=65;//decimal value
		System.out.println("char1:"+ch1);
		
		char ch2;
		ch2=0101;//octal value
		System.out.println("char2:"+ch2);
		
		char ch3;
		ch3=0x41;//hexa decimal value
		System.out.println("char3:"+ch3);
		
		char ch4;
		ch4=0b000001;//binary value
		System.out.println("char4:"+ch4);
		
		char ch5;
		ch5='\u0041';//uniq value
		System.out.println("char5:"+ch5);
		
		
	}
}

		
		
		
	